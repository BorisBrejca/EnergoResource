    answerWrapper = document.querySelector('.answer');
    answerBlock = answerWrapper.querySelectorAll('.answerblock')
// Место под замену
const questionsWrapper = document.querySelector('.questions');


function clearPage(){
    questionsWrapper.innerHTML = '';
}
// Стереть страницу

answerBlock.forEach((el) => {
    el.addEventListener('click', function showQuestions(){
        answerBlock.forEach((el) => {
            el.classList.remove('activeAnswerBlock')
        })
        el.classList.add('activeAnswerBlock')

        clearPage()
        let newMass = eval('arr' + el.dataset.mass)

        for (let index = 0; index < newMass.length; index++) {

        maketHtml = `                     
<div class="question">
                <h1>%question%</h1>
<div class="svgBlock">
</div>
</div>
<div class="question-answer">
                <h1>%answer%</h1>
            
    </div>`; // Шаблон вырванный из HTML

        oldHTML = maketHtml.replace('%question%',newMass[index]['question'])
        newHTML = oldHTML.replace('%answer%', newMass[index]['answer'])

        questionsWrapper.innerHTML += newHTML
        }
    })
})
